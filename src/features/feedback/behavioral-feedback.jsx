function Section({ title, icon, children }) {
  return (
    <div>
      <div className="flex items-center gap-2 mb-2">
        {icon}
        <h3 className="text-lg font-semibold">{title}</h3>
      </div>
      <Separator className="mb-4" />
      {children}
    </div>
  );
}

function AttributeList({ rating }) {
  const attributes = {
    communication: <MessageSquare className="h-4 w-4 text-muted-foreground" />,
    professionalism: (
      <HeartHandshake className="h-4 w-4 text-muted-foreground" />
    ),
    attitude: <Smile className="h-4 w-4 text-muted-foreground" />,
    culturalFit: <Users className="h-4 w-4 text-muted-foreground" />,
  };

  return (
    <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
      {Object.entries(rating).map(([key, value]) => (
        <div
          key={key}
          className="flex items-center justify-between border rounded-lg p-3"
        >
          <div className="flex items-center gap-2">
            {attributes[key]}
            <span className="capitalize">{key.replace(/([A-Z])/g, " $1")}</span>
          </div>
          <Badge className="bg-primary/10 text-primary">{value}/10</Badge>
        </div>
      ))}
    </div>
  );
}

function ScoreCard({ score }) {
  const num = parseFloat(score);
  const getColor = (val) => {
    if (val >= 8) return "text-green-600";
    if (val >= 6) return "text-amber-600";
    return "text-red-600";
  };

  const getProgressColor = (val) => {
    if (val >= 8) return "bg-green-600";
    if (val >= 6) return "bg-amber-600";
    return "bg-red-600";
  };

  const color = getColor(num);
  const progressColor = getProgressColor(num);

  return (
    <div className="flex flex-col md:flex-row items-center justify-between gap-4">
      <div className="flex items-center gap-3">
        <div className={`text-5xl font-bold ${color}`}>{score}</div>
        <div className="text-muted-foreground">
          <div className="text-sm uppercase font-semibold">Overall Score</div>
          <div className="text-xs">Behavioral compatibility</div>
        </div>
      </div>
      <div className="w-full md:w-2/3">
        <div className="flex justify-between text-xs mb-1">
          <span>Needs Work</span>
          <span>Excellent</span>
        </div>
        <Progress
          value={num * 10}
          className="h-3"
          indicatorClassName={progressColor}
        />
      </div>
    </div>
  );
}

function RecommendationCard({ recommendation }) {
  const isRecommended = recommendation.verdict === "Yes";

  return (
    <Card
      className={
        isRecommended
          ? "border-green-200 bg-green-50 dark:border-green-900 dark:bg-green-900/20"
          : "border-red-200 bg-red-50 dark:border-destructive/30 dark:bg-destructive/10"
      }
    >
      <CardHeader>
        <div className="flex items-center gap-2">
          {/* <ThumbsUp
            className={`h-5 w-5 ${isRecommended ? "text-green-600" : "text-red-600"
              }`}
          /> */}
          <CardTitle className="text-lg">Recommendation</CardTitle>
        </div>
      </CardHeader>
      <CardContent>
        <div className="flex items-center gap-3 mb-3">
          <h3
            className={`text-xl ${isRecommended
              ? "text-green-700 dark:text-gray-200 font-semibold"
              : "text-red-700 dark:text-gray-200"
              }`}
          >
            {isRecommended ? "" : "Verdict"}
            {isRecommended ? "Recommended for Hire" : ""}
          </h3>
          <Badge
            className={
              isRecommended
                ? "bg-green-200 text-green-800 dark:bg-green-800/30 dark:text-green-300"
                : "bg-red-200 text-red-800 dark:bg-red-700/30 dark:text-red-100"
            }
          >
            {recommendation.verdict}
          </Badge>
        </div>

        <Separator className={
          isRecommended
            ? "bg-green-200 dark:bg-green-100/10"
            : "bg-red-200 dark:bg-destructive/10"
        }
        />

        <div className="mt-3">
          <p
            className={
              isRecommended
                ? "text-green-800 dark:text-gray-200"
                : "text-red-800 dark:text-gray-300"
            }
          >            {recommendation.reasoning}
          </p>
        </div>
      </CardContent>
    </Card>
  );
}

